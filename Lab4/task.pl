% normalize_words(+RawList, -AtomsList)
% Преобразует элементы входного списка в атомы:
% - если уже atom -> не трогаем
% - если string -> atom_string/2
% - если список кодов или символов -> atom_codes/2
% - иначе term_to_atom/2 fallback
normalize_words([], []).
normalize_words([H|T], [Hatom|T2]) :-
    normalize_word(H, Hatom),
    normalize_words(T, T2).

normalize_word(H, H) :-
    atom(H), !.
normalize_word(H, Atom) :-
    string(H), !,
    atom_string(Atom, H).
normalize_word(H, Atom) :-
    is_list(H), !,
    % список кодов или символов -> atom_codes
    (   catch(atom_codes(Atom, H), _, fail) -> true
    ;   term_to_atom(H, Atom)
    ).
normalize_word(H, Atom) :-
    % fallback: любое прочее представление (числа, структуры)
    term_to_atom(H, Atom).

% ---------------------------------------------------------
% АНАЛИЗАТОРЫ ТЕРМИНАЛОВ (как в лекции)
% ---------------------------------------------------------

an_qw([X], qw(X)) :-
    q_list(L),
    member(X, L).

% глагол: либо из словаря (каноническая форма), либо канонический == сама форма
an_v([X], v(V)) :-
    v_list(L),
    member([X, V], L), !.
an_v([X], v(X)).   % fallback: если глагола нет в словаре — использовать саму форму

an_n([X], n(X)) :-
    n_list(L),
    member(X, L).

an_name([X], name(X)) :-
    atom(X),
    atom_chars(X, [C|_]),
    char_type(C, upper),          % начинается с заглавной буквы
    \+ is_question_word(X),
    \+ is_verb(X),
    \+ is_noun(X).

% вспомогательные предикаты
is_question_word(X) :-
    q_list(L),
    member(X, L).

is_verb(X) :-
    v_list(L),
    member([X,_], L).

is_noun(X) :-
    n_list(L),
    member(X, L).

% ---------------------------------------------------------
% ГЛАВНЫЙ ПРЕДИКАТ (с нормализацией)
% ---------------------------------------------------------

% an_q(+RawWords, -Res)
% RawWords может содержать атомы или строки; в конце ожидается '?'
an_q(RawWords, Res) :-
    normalize_words(RawWords, Words),        % нормализуем вход
    append(WordsNoQ, ['?'], Words),          % отрезаем вопросительный знак
    an_q_phrase(WordsNoQ, Res), !.

% ---------------------------------------------------------
% АНАЛИЗ ВОПРОСНЫХ ФРАЗ (в стиле лекции)
% ---------------------------------------------------------

% --- Кто ... (агент неизвестен)
an_q_phrase(L, Res) :-
    append(P1, P2, L),
    an_qw(P1, qw('Кто')),
    append(P3, P4, P2),
    an_v(P3, v(V)),
    an_n(P4, n(Obj)),
    Res =.. [V, agent(_), object(Obj)].

% --- Что ... (объект неизвестен)
an_q_phrase(L, Res) :-
    append(P1, P2, L),
    an_qw(P1, qw('Что')),
    append(P3, P4, P2),
    an_v(P3, v(V)),
    an_name(P4, name(Name)),
    Res =.. [V, agent(Name), object(_)].

% --- Где ... (локация неизвестна)
an_q_phrase(L, Res) :-
    append(P1, P2, L),
    an_qw(P1, qw('Где')),
    append(P3, P4, P2),
    an_v(P3, v(V)),
    an_n(P4, n(Obj)),
    Res =.. [V, object(Obj), loc(_)].

